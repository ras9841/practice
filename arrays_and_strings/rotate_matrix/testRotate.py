from rotate import rotate90

def main():
    # n = 0
    assert rotate90([],0) == []

    # n = 1
    assert rotate90([1],1) == [1]

    # n = 2
    assert rotate90([[1,3],[2,4]],2) == [[2,1],[4,3]]

    # n = 3
    M = [[3, 2, 1], [6, 5, 4], [9, 8, 7]]
    assert rotate90([[1,4,7],[2,5,8],[3,6,9]],3) == M

    # n = 4
    M = [[4, 3, 2, 1], [8, 7, 6, 5], [12, 11, 10, 9], [16, 15, 14, 13]]
    assert rotate90([[1,5,9,13],[2,6,10,14],[3,7,11,15],[4,8,12,16]],4) == M

main()
